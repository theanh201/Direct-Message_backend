create database Direct_Backend_DB;
use Direct_Backend_DB;

DROP TABLE IF EXISTS USER_ACCOUNT;
CREATE TABLE USER_ACCOUNT (
    USER_ACCOUNT_USERNAME CHAR(64) UNIQUE,
    USER_ACCOUNT_PASSWORD CHAR(64),
    USER_ACCOUNT_ID INT UNIQUE AUTO_INCREMENT,
    PRIMARY KEY (USER_ACCOUNT_ID)
);
SELECT * FROM USER_ACCOUNT;

CREATE TABLE USER_TOKEN (
    USER_TOKEN_ID CHAR(64),
	USER_ACCOUNT_ID INT,
    USER_TOKEN_EXPIRE_DATE DATETIME,
    FOREIGN KEY (USER_ACCOUNT_ID)
        REFERENCES USER_ACCOUNT (USER_ACCOUNT_ID),
	PRIMARY KEY (USER_ACCOUNT_ID, USER_TOKEN_ID)
);
SELECT * FROM USER_TOKEN;

